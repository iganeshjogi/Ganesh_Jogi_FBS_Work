#### Sequential 

#1. list
x = [10, 20, 30, 40 ,50]
print(type(x))

#2. tuple
x = (10, 20, 30, 40, 50)
print(type(x))

#3. range
x = range(1,100)
print(type(x))


#### set type

#1. set
x = {10, 20, 30, 40, 50}
print(type(x))

#2. frozenset
x = frozenset({10, 20, 30, 40, 50})
